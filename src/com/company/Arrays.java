package com.company;

public class Arrays {
    public void print(int[] in) {
        for(int j = 0; j < in.length; j++) {
            System.out.println(in[j]);
        }
    }    public static void print(String[] in) {
        for(int j = 0; j < in.length; j++) {
            System.out.println(in[j]);
        }
    }
}
